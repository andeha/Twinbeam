with_live_update:
    j 0xBE005000 /* When 'live update' is utilized by toggling NVMCON.PFSWAP. */
wo_live_update:
    j 0xBD001000 /* Without 'live update', 𝘪․𝘦 one memory area. */
    nop
    END(__start)
    
    .org 0x0480
    START(boot_Debug)
boot_Debug:
    j 0xBD000910
    nop
again:
    nop
    j again
    END(boot_Debug)
    
