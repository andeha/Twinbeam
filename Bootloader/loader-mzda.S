#define START(name) .set noat; .set noreorder; .ent name
#define END(name) .size name,.-name; .end name; .set reorder; .set at
#define MACRO(name) .set noat; .set noreorder; .macro name
#define ENDM(name) .endmacro

#define c0_badvaddr $8, 0
#define c0_count $9, 0
#define c0_compare $11, 0
#define c0_cause $13, 0
#define c0_epc $14, 0
#define c0_ebase $15, 1
#define c0_config1 $16, 1
#define c0_watchlo $18, 0
#define c0_watchhi $19, 0
    
    .section .text
    
    .org 0x0000
    START(__start)
__start:
    la       $gp, _gp                   /* ⬷ stack begin */
    la       $sp, _sp                   /* ⬷ stack end */
    
    di                                  /* disable all interrupts. */
    
    mtc0     $zero, c0_count            /* clear to measure 'time since boot'. */
    
    lui      $t0, 0xbd00                /* assign 🔧Ebase(0xBD000000); and must be modulo 4096. */
    addiu    $t0, $t0, 0x0000
    mtc0     $t0, c0_ebase
    nop
    
    mfc0     $t0, c0_config1            /* read the bits from Cop0's 'Config' register. */
    ext      $t1, $t0, 3, 1             /* retrieve 1 bit(s) from bit-loc 3 and upward from t0 and store word in t1. */
                                        /* i.e retrieve the bit 'Watch registers implemented'. */
    beq      $t1, $0, done_watch        /* branch in-case the third bit in c0_config is equal to '0'. */
    li       $t1, 0x7                   /* clear I, R and W conditions bits. */
    
    /* the 'WatchHi' and 'WatchLo' must be initialized to avoid spurious 
      exceptions after ERL=0. 
    
     ⤐ clear Watch Status bits and disable watch exceptions ⤐ */
    
    mtc0     $t1, c0_watchhi            /* write C0_WatchHi0 */
    mfc0     $t0, c0_watchhi            /* read C0_WatchHi0 */
    bne      $zero, $t0, done_watch
    mtc0     $zero, c0_watchlo          /* write C0_WatchLo0 */
done_watch:
    /* IV, and software interrupts. */
    mtc0     $zero, c0_cause            /* reset Cop0's 'Cause' AFTER initialization of opt. WatchHi/Lo. */
    mtc0     $zero, c0_compare
    
/*
    
    __builtin_uint_t status = 🔎Status();
    const __builtin_uint_t statusOff = MIPS_Status_BEV;
    const __builtin_uint_t statusOn = 0;
    status &= ~statusOff; status |= statusOn;
    🔧Status(status);
    
    mfc0     $t0, _CP0_STATUS
    and      $t0, $t0, 0xffbfffff       / * clear BEV. * /
    mtc0     $t0, _CP0_STATUS
    
 */
    
    mfc0     $t0, $12, 0
    nop
    
    lui      $t1, ~0x0040               /* MIPS_Status_BEV = 0b1 << 22 */
    addiu    $t1, $t1, 0xFFFF
    and      $t0, $t0, $t1
    mtc0     $t0, $12, 0
    nop
    
/*
    
