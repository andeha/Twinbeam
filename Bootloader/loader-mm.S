/* Parameters smaller than 32-bits are promoted to 32-bits. */
/* 64-bits parameters are for little-endian passed in e.g a1:a0 and a3:a2. */

/* compile with 'ninja -f build-mm.ninja'. */
/* ../../../../mips-tools/12.0.0/llvm-objdump --headers ../bootloader-mm */
/* ../../../../mips-tools/12.0.0/llvm-objdump --disassemble ../bootloader-mm */
/* start with '../../Sprinkle/pdb/pdb -s' alternatively */
/* start with '/Applications/microchip/mplabx/v5.50/mplab_platform/bin/mdb.sh'. */
/* select with 'device PIC32MM0064GPL036' and 'hwtool sim'. */
/* add ':00000001FF' to the end of the boot .hex file generated by llvm2pic32. */
/* load with 'program bootloader-mm_c2eaaa2ad.hex' alt. 'program bootloader-mm'. */
/* list machine instructions with 'x /p100i 0xbfc00000'. */
/* other commands : Break *0x108 stepi next backtrace halt 
 watch 0xa0007ff0 R */

#define START(name) .set at; .set noreorder; .ent name
#define END(name) .size name,.-name; .end name; .set reorder; .set noat
#define MACRO(name) .set at; .set noreorder; .macro name
#define ENDM(name) .endmacro

#define c0_count $9, 0
#define c0_compare $11, 0
#define c0_status $12, 0
#define c0_cause $13, 0
#define c0_ebase $15, 1
#define c0_config1 $16, 1
    
    .section .text
    
    .org 0x0000
    START(__start)
__start:
    la       $gp, _gp                   /* ⬷ stack begin */
    la       $sp, _sp                   /* ⬷ stack end */
    
    di                                  /* disable all interrupts. */
    
    mtc0     $zero, c0_count            /* clear to measure 'time/cycles since boot'. */
    
    lui      $t0, 0xbd00                /* assign 🔧Ebase(0xBD000000); and must be modulo 4096. */
    addiu    $t0, $t0, 0x0000
    mtc0     $t0, c0_ebase
    nop
    
    mtc0     $zero, c0_cause            /* reset Cop0's 'Cause' AFTER initialization of opt. WatchHi/Lo. */
    mtc0     $zero, c0_compare
    
  /*
    
    __builtin_uint_t status = 🔎Status();
    const __builtin_uint_t statusOff = MIPS_Status_BEV;
    const __builtin_uint_t statusOn = 0;
    status &= ~statusOff; status |= statusOn;
    🔧Status(status);
    
  */
    
    mfc0     $t0, c0_status
    and      $t0, $t0, 0xffbfffff       /* ⬷ clear BEV pattern. */
    mtc0     $t0, c0_status
    
    j 0xBD000000 /* ⬷ no 'live update' 𝘪․𝘦 one memory area. */
    nop
    END(__start)
    
    .org 0x0480
    START(boot_Debug)
boot_Debug:
    nop
again:
    nop
    j again
    END(boot_Debug)
    
